package com.cts.java;

public class PrintingNumber {
		public void design(int a,int b,int c,int d)
		{

			for(int i=0;i>a;i++)
			{
			System.out.println(i+"\b"+"\n");	
			}
			for(int j=0;j>b;j++)
			{
			System.out.println(j+"\b");	
			}
			for(int k=0;k<c;k++)
			{
			System.out.println(k+"\b");	
			}
			for(int l=0;l>d;l++)
			{
			System.out.println(l+"\b");	
			}
		}

}
